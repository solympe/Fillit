#include "fillit.h"

int	ft_sqrt(int nb)
{
	int i;
	int temp;

	i = 0;
	while (i * i < nb)
	{
		temp = i;
		i++;
	}
	if (i * i == nb)
		return (i);
	else
		return (temp);
}

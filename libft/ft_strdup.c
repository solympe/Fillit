#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dest;
	size_t	len;

	len = ft_strlen(s1);
	if (!(dest = ft_strnew(len)))
		return (NULL);
	ft_strcpy(dest, s1);
	return (dest);
}
